# - lui / auipc
# - imediato zero
# - imediato positivo
# - imediato negativo
# - shamt zero
# - shamt não zero
# - shamt alto
# - tentativa de escrita em x0 com instruções I-type e R-type

# ------------------------------------------------------------
# Base da Fase 1
# ------------------------------------------------------------
addi x1,  x0, 15        # x1 = 15
addi x2,  x0, -8        # x2 = -8
addi x3,  x0, 3         # x3 = 3

addi x0,  x1, 7         # x0 deve continuar 0
add  x4,  x1, x3        # x4 = 18
sub  x5,  x3, x1        # x5 = -12
add  x0,  x1, x3        # x0 deve continuar 0

# ------------------------------------------------------------
# Operações lógicas I-type
# ------------------------------------------------------------
andi x6,  x1, 0         # x6 = 0
andi x7,  x1, 6         # x7 = 6
ori  x8,  x6, -1        # x8 = 0xFFFF_FFFF
xori x9,  x1, -1        # x9 = ~15

# ------------------------------------------------------------
# Comparações I-type
# ------------------------------------------------------------
slti  x10, x2, 0        # x10 = 1  (-8 < 0)
sltiu x11, x3, 4        # x11 = 1  (3 < 4)

# ------------------------------------------------------------
# Operações lógicas R-type
# ------------------------------------------------------------
and  x12, x1, x3        # x12 = 3
or   x13, x6, x3        # x13 = 3
xor  x14, x1, x3        # x14 = 12

# ------------------------------------------------------------
# Comparações R-type
# ------------------------------------------------------------
slt  x15, x2, x1        # x15 = 1  (-8 < 15)
sltu x16, x2, x1        # x16 = 0  (unsigned: 0xFFFF_FFF8 < 15 = falso)

# ------------------------------------------------------------
# Shifts I-type
# ------------------------------------------------------------
slli x17, x3, 0         # x17 = 3
slli x18, x3, 4         # x18 = 48
srli x19, x18, 1        # x19 = 24
srai x20, x2, 2         # x20 = -2
slli x21, x3, 16        # x21 = 0x0003_0000

# ------------------------------------------------------------
# Shifts R-type
# ------------------------------------------------------------
sll  x22, x3, x1        # x22 = 3 << 15
srl  x23, x18, x3       # x23 = 6
sra  x24, x2, x3        # x24 = -1

# ------------------------------------------------------------
# U-type
# ------------------------------------------------------------
lui   x25, 0x8          # x25 = 0x00008000
auipc x26, 0            # x26 = PC atual