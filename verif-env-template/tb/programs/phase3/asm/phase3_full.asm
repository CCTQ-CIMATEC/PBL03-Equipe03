# Fase 3 completa
# Cobre:
# - sw / lw
# - sb / sh
# - lb / lbu
# - lh / lhu
# - offset zero
# - offset positivo
# - offset negativo real na instrução
# - sign extension
# - zero extension
# - load com resultado zero

addi x1,  x0, 64      # base = 64
addi x2,  x0, 18      # dado positivo

# ------------------------------------------------------------
# SW / LW
# ------------------------------------------------------------
sw   x2,  0(x1)       # mem[64] = 18
lw   x3,  0(x1)       # x3 = 18

# ------------------------------------------------------------
# SB / LB / LBU
# ------------------------------------------------------------
addi x4,  x0, -1      # x4 = 0xFFFF_FFFF
sb   x4,  4(x1)       # mem[68] = 0xFF
lbu  x5,  4(x1)       # x5 = 0x000000FF
lb   x6,  4(x1)       # x6 = 0xFFFFFFFF

# ------------------------------------------------------------
# SH / LH / LHU
# ------------------------------------------------------------
addi x7,  x0, -16     # x7 = 0xFFFF_FFF0
sh   x7,  8(x1)       # mem[72..73] = 0xFFF0
lhu  x8,  8(x1)       # x8 = 0x0000FFF0
lh   x9,  8(x1)       # x9 = 0xFFFFFFF0

# ------------------------------------------------------------
# Mais um caso de SW/LW com offset positivo
# ------------------------------------------------------------
sw   x2,  12(x1)      # mem[76] = 18
lw   x10, 12(x1)      # x10 = 18

# ------------------------------------------------------------
# SB com offset positivo não nulo
# ------------------------------------------------------------
sb   x2,  1(x1)       # mem[65] = 0x12
lbu  x11, 1(x1)       # x11 = 0x00000012

# ------------------------------------------------------------
# Offset negativo real na própria instrução
# ------------------------------------------------------------
addi x12, x1, 4       # x12 = 68
sw   x2, -4(x12)      # mem[64] = 18   -> store_off_neg
lw   x13, -4(x12)     # x13 = 18       -> load_off_neg

# ------------------------------------------------------------
# Load com resultado zero
# ------------------------------------------------------------
lbu  x14, 2(x1)       # mem[66] = 0x00 -> x14 = 0